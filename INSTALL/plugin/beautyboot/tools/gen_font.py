for c in range(0x20, 0x7F):
    if c not in FONT_DATA:
        FONT_DATA[c] = [0x00]*8

def section(tag, data):
    return tag.encode('ascii') + struct.pack('>I', len(data)) + data

def build_pf2():
    glyphs = sorted(FONT_DATA.items())
    data_bytes = b""
    offsets = {}
    for cp, bitmap in glyphs:
        offsets[cp] = len(data_bytes)
        glyph_header = struct.pack('>HHhhH', 8, 8, 0, -1, 8)
        data_bytes += glyph_header + bytes(bitmap)
    chix_bytes = b""
    for cp, _ in glyphs:
        chix_bytes += struct.pack('>IBI', cp, 0, offsets[cp])
    pf2  = b"FILE\x00\x00\x00\x04PFF2"
    pf2 += section("NAME", b"BeautyBoot 8\x00")
    pf2 += section("PTSZ", struct.pack('>H', 16))
    pf2 += section("MAXW", struct.pack('>H', 8))
    pf2 += section("MAXH", struct.pack('>H', 8))
    pf2 += section("ASCE", struct.pack('>H', 7))
    pf2 += section("DESC", struct.pack('>H', 1))
    pf2 += section("CHIX", chix_bytes)
    pf2 += section("DATA", data_bytes)
    return pf2

